<?php

namespace App\Filament\Resources;

use App\Filament\Resources\PurchaseOrderResource\Pages;
use App\Filament\Resources\PurchaseOrderResource\RelationManagers;
use App\Models\PurchaseOrder;
use Filament\Forms;
use Filament\Forms\Form;
use Filament\Resources\Resource;
use Filament\Tables;
use Filament\Tables\Table;
use Illuminate\Database\Eloquent\Builder;
use Illuminate\Database\Eloquent\SoftDeletingScope;

class PurchaseOrderResource extends Resource
{
    protected static ?string $model = PurchaseOrder::class;

    protected static ?string $navigationIcon = 'heroicon-o-rectangle-stack';

    public static function form(Form $form): Form
    {
        return $form
            ->schema([
                Forms\Components\Select::make('service_id')->label('Service')->required()->createOptionForm([
                    Forms\Components\TextInput::make('name')->label('Service name')->autofocus()->required()->maxLength(20),
                    Forms\Components\Textarea::make('description')->label('Service description')->autofocus(),
                ]),
                Forms\Components\TextInput::make('owner_firstname')->label('Owner firstname')->required()->maxLength(30),
                Forms\Components\TextInput::make('owner_lastname')->label('Owner lastname')->required()->maxLength(30),
                Forms\Components\TextInput::make('owner_phone_number')->label('Owner phone number')->tel()->required(),
                Forms\Components\TextInput::make('total_price')->label('Total price')->required()->numeric()->prefix('$'),

            ]);
    }

    public static function table(Table $table): Table
    {
        return $table
            ->columns([
                //
            ])
            ->filters([
                //
            ])
            ->actions([
                Tables\Actions\EditAction::make(),
            ])
            ->bulkActions([
                Tables\Actions\BulkActionGroup::make([
                    Tables\Actions\DeleteBulkAction::make(),
                ]),
            ]);
    }

    public static function getRelations(): array
    {
        return [
            //
        ];
    }

    public static function getPages(): array
    {
        return [
            'index' => Pages\ListPurchaseOrders::route('/'),
            'create' => Pages\CreatePurchaseOrder::route('/create'),
            'edit' => Pages\EditPurchaseOrder::route('/{record}/edit'),
        ];
    }
}
